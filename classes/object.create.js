'use strict'

const User = {

  init(email, password) {
    this.email = email;
    this.password = password;
  },
  log() {
    console.log('User')
  }
}

const user = Object.create(User)



user.log()

user.name = 'Vladimir'
user.age = 25
user.init('ya@ya.ya', '123456')
console.log(user)
console.log(user.__proto__ === User)

const admin = Object.create(user)
console.log(admin)

